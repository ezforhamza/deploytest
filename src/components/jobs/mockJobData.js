// Helper function to get jobs by company name
export const getJobsByCompany = (companyName) => {
  return mockJobs.filter(job => job.companyName === companyName);
};

// Helper function to find job by ID
export const getJobById = (jobId) => {
  return mockJobs.find(job => job.id === jobId);
};

// Helper function to get job data for company profiles
export const getCompanyJobsData = (companyName) => {
  const companyJobs = getJobsByCompany(companyName);
  
  // Transform to match the expected company profile job structure
  return companyJobs.map(job => ({
    id: job.id,
    title: job.jobTitle,
    department: "Engineering", // Default department, could be enhanced
    location: job.workType.replace(/[🌍🏢⏰📝]/g, '').trim(),
    type: job.jobType.replace(/[👔⏰📝]/g, '').trim(),
    experience: job.level.replace(/[🚀🧠🌱📌]/g, '').trim(),
    salary: job.salary,
    description: `Join our team as a ${job.jobTitle}. We're looking for talented individuals to contribute to our innovative projects and grow their career with us.`,
    requirements: [
      "Strong technical skills",
      "Team collaboration experience", 
      "Problem-solving abilities",
      "Communication skills"
    ],
    postedDate: job.datePosted,
    applicants: Math.floor(Math.random() * 100) + 10, // Random number of applicants
    // Keep original job data for easy reference
    originalJobData: job
  }));
};
